public class OneDimArrayTest
{
	public static void main(String[] args){
		int rollno[] = new int[5];
		
		rollno [0] = 0;
		rollno [1] = 0;
		rollno [2] = 0;
		rollno [3] = 0;
		rollno [4] = 0;
		
		System.out.println("-----------Start Array Printing -------------------");
		
		for (int i=0;i<5;i++)
		{
			System.out.println("Element at Index "+i+" is "+rollno[i]);
		}
		System.out.println("-----------Start Array Printing -------------------");
	}
}